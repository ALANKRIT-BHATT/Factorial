x=int(input("Enter day of week "))
if x==1:
    print(f"Day {x} of the week is Monday")
elif x==2:
    print(f"Day {x} of the week is Tuesday")
elif x==3:
    print(f"Day {x} of the week is Wednesday")
elif x==4:
    print(f"Day {x} of the week is Thursday")
elif x==5:
    print(f"Day {x} of the week is Friday")
elif x==6:
    print(f"Day {x} of the week is Saturday")
elif x==7:
    print(f"Day {x} of the week is Sunday")
else:
    print(f"Day {x} of the week is Invalid")     
